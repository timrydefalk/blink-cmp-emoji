    ["ÿ"] = "y",
    ["ñ"] = "n",
    ["ç"] = "c",
}

---@param input string
---@return string
local function normalize(input)
    for accented, plain in pairs(transliterations) do
        input = input:gsub(accented, plain)
    end
    return input
end

---@param description string
---@return string
local function make_label(description)
    local normalized = normalize(description)
        :gsub("[ %-]", "_")
        :gsub("[%.&:,']", "")
        :gsub("\u{201C}", "")
        :gsub("\u{201D}", "")
        :gsub("_+", "_")
        :lower()
    return ":" .. normalized .. ":"
end

